package com.snframework.pojo.serialization;

public class CreateCRSerialization {

	private String description;
	private String short_description;
	private String urgency;
	private String active;
	private String close_notes;
	
	
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getShort_description() {
		return short_description;
	}
	public void setShort_description(String short_description) {
		this.short_description = short_description;
	}
	public String getUrgency() {
		return urgency;
	}
	public void setUrgency(String urgency) {
		this.urgency = urgency;
	}
	public String getActive() {
		return active;
	}
	public void setActive(String active) {
		this.active = active;
	}
	public String getClose_notes() {
		return close_notes;
	}
	public void setClose_notes(String close_notes) {
		this.close_notes = close_notes;
	}

	
}
